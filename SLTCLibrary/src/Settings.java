    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String user = "root";
        String pass = "";
        String[] restoreCmd = new String[]{"C:/xampp/mysql/bin/mysqldump.exe" , "--user="+ user,"--password=" + pass,"-e","source "+ path};

        Process process;
        try {
            process = Runtime.getRuntime().exec(restoreCmd);

            int procCon = process.waitFor();
            if (procCon == 0) {
                JOptionPane.showMessageDialog(null, "restored Successfully! ");
                // jLabel1.setText("Backup created Successfully! ");
            } else {
                JOptionPane.showMessageDialog(null, "Can't Restore ");
                //jLabel1.setText("Can't create backup ");
            }
        } catch (IOException ex) {
            Logger.getLogger(Settings.class.getName()).log(Level.SEVERE, null, ex);
        } catch (InterruptedException ex) {
            Logger.getLogger(Settings.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

