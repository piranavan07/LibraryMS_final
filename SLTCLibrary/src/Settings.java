    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Process p = null;
        try {
            Runtime runtime = Runtime.getRuntime();
            p = runtime.exec("C:/xampp/mysql/bin/mysqldump.exe --userroot --password --database librarydb -r" + path);

            int processComplete = p.waitFor();
            if (processComplete == 0) {
                JOptionPane.showMessageDialog(null, "Backup created Successfully! ");
                // jLabel1.setText("Backup created Successfully! ");
            } else {
                JOptionPane.showMessageDialog(null, "Can't create backup ");
                //jLabel1.setText("Can't create backup ");
            }

        } catch (HeadlessException | IOException | InterruptedException e) {
            JOptionPane.showMessageDialog(null, "Error at Backuprestore" + e.getMessage());
        }

    }//GEN-LAST:event_jButton1ActionPerformed

